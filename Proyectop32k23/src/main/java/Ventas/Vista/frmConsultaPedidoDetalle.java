     public void llenadoDeTablasPedidos(int cotidSeleccionado) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id Pedido");
        modelo.addColumn("Codigo Producto");
        modelo.addColumn("Precio");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Sub Total");
        clsPedidos cotizacion = new clsPedidos();
        List<clsPedidos> listaCotizaciones = cotizacion.getListadoPedidosDetalle(cotidSeleccionado);
        tblPedDetCons.setModel(modelo);
        String[] dato = new String[5];
        for (int i = 0; i < listaCotizaciones.size(); i++) {
            dato[0] = Integer.toString(listaCotizaciones.get(i).getIdPed());
            dato[1] = Integer.toString(listaCotizaciones.get(i).getIdProducto());
            dato[2] = Double.toString(listaCotizaciones.get(i).getPrecioProducto());
            dato[3] = Integer.toString(listaCotizaciones.get(i).getCantidadProducto());
            dato[4] = Double.toString(listaCotizaciones.get(i).getTotalPed());
            
            modelo.addRow(dato);
        }              

                    

}
    


    

