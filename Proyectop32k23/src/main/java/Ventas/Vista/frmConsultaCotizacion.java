     public void llenadoDeTablasCotizaciones() {
        DefaultTableModel modelo = new DefaultTableModel(){
            public boolean isCellEditable(int row, int column) {
                return false; // Desactivar la edici贸n de las celdas
            }
        };
        modelo.addColumn("Id Cotizacion");
        modelo.addColumn("Id Cliente");
        modelo.addColumn("Id Vendedor");
        modelo.addColumn("Fecha");
        modelo.addColumn("Total");
        clsCotizacion cotizacion = new clsCotizacion();
        //VendedorDAO vendedorDAO = new VendedorDAO();
        List<clsCotizacion> listaCotizaciones = cotizacion.getListadoCotizaciones();
        tblCotConsulta.setModel(modelo);
        String[] dato = new String[5];
        for (int i = 0; i < listaCotizaciones.size(); i++) {
            dato[0] = Integer.toString(listaCotizaciones.get(i).getIdCot());
            dato[1] = Integer.toString(listaCotizaciones.get(i).getIdCliente());
            dato[2] = Integer.toString(listaCotizaciones.get(i).getIdVendedor());
            dato[3] = listaCotizaciones.get(i).getFechaCot();
            dato[4] = Double.toString(listaCotizaciones.get(i).getTotalCot());
            modelo.addRow(dato);
        }              

                    

}
    
