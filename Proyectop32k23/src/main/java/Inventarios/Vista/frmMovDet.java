    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("IDEncabezados");
        modelo.addColumn("IDProducto");
        modelo.addColumn("Muestras");
        modelo.addColumn("Cantidades");
        modelo.addColumn("Traslados");
       
        clsmovDetalle movimientos = new clsmovDetalle(); 
        //VendedorDAO vendedorDAO = new VendedorDAO();
        List<clsmovDetalle> listadoDetalles = movimientos.getListadoDetalles();      
        tablaMovimientos.setModel(modelo);
        String[] dato = new String[8];
        for (int i = 0; i < listadoDetalles.size(); i++) {
            dato[0] = Integer.toString(listadoDetalles.get(i).getIdEncabezado());
            dato[2] = Integer.toString(listadoDetalles.get(i).getIdProducto());
            dato[3] = Integer.toString(listadoDetalles.get(i).getCantidadesDetalles());
            dato[4] = Integer.toString(listadoDetalles.get(i).getMuestrasDetalles());
            dato[5] = Integer.toString(listadoDetalles.get(i).getTrasladosDetalles());
            modelo.addRow(dato);
        }    
        DefaultTableModel modelos = new DefaultTableModel();
         modelos.addColumn("IdMovimientos");
         modelos.addColumn("Fecha");
         modelos.addColumn("Estatus");
         clsmovEncabezados movimiento = new clsmovEncabezados();
        List<clsmovEncabezados> listadoEncabezados = movimiento.getListadoMovimientos();
        tablaMovimientos.setModel(modelo);
        String[] datos = new String[8];
        for (int i = 0; i < listadoEncabezados.size(); i++) {
            datos[1] = Integer.toString(listadoEncabezados.get(i).getIdMovimiento());
            datos[6] = listadoEncabezados.get(i).getFechaMovimiento();
            datos[7] = listadoEncabezados.get(i).getEstatusMovimiento();
            modelo.addRow(dato);
        }    
    }

