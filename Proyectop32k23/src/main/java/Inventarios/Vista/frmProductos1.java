    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Existencia");
        modelo.addColumn("Codigo Linea");
        modelo.addColumn("Codigo Marca");
        modelo.addColumn("Precio");
        modelo.addColumn("Estatus");
        clsProductos productos = new clsProductos();
        //VendedorDAO vendedorDAO = new VendedorDAO();
        List<clsProductos> listaProductos = productos.getListadoProductos();
        tablaBodegas.setModel(modelo);
        String[] dato = new String[7];
        for (int i = 0; i < listaProductos.size(); i++) {
            dato[0] = Integer.toString(listaProductos.get(i).getIdProductos());
            dato[1] = listaProductos.get(i).getNombreProductos();
            dato[2] = listaProductos.get(i).getExistenciaProducto();
            dato[3] = listaProductos.get(i).getLineaCodigo();
            dato[4] = listaProductos.get(i).getMarcaCodigo();
            dato[5] = listaProductos.get(i).getPrecioProducto();
            dato[6] = listaProductos.get(i).getEstatusProducto();
            modelo.addRow(dato);
        }       
    }

